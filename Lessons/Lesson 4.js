// we can use a while loop to create a countdown
let num = 10;
while (num >= 0) {
	console.log(num);
	num--;
}

// Here's an array:
const names = [ "Matthew", "James", "Cyrus", "Aditya", "Elizabeth", "Katy" ];

// Can you use a for loop to print each of these names to the console?
for (let i = 0; i < names.length; i++) {
  console.log(names[i]);
}























































































