// Can you write a function called isEven that
// returns a boolean indicating whether the
// number input is even or odd. Hint: a number
// is even if it is divisible by 2... meaning
// when divided by 2, the remainder will be 0...
function isEven (a) {
  if (a % 2 == 0) {
    return true;
  } else {
    return false;
  }
}
